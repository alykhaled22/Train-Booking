    public void AddTrip(int id,String Tname,String date,int D,String S,String E,String station){
        
        DbConnection databaseConnection = new DbConnection();
        Connection connection = databaseConnection.getConnection();


        if (connection != null) {
            String insertQuery = "INSERT INTO TRIP(TRIP_ID,TRAIN_NAME,TRIP_DATE,TRIP_DURATION,START_POINT,END_POINT,station_name) VALUES (?,?,?,?,?,?,?)";

            try (PreparedStatement preparedStatement = connection.prepareStatement(insertQuery)) {
                preparedStatement.setInt(1, id);
                preparedStatement.setString(2, Tname);
                preparedStatement.setString(3, date);
                preparedStatement.setInt(4, D);
                preparedStatement.setString(5, S);
                preparedStatement.setString(6, E);
                preparedStatement.setString(7, station);


                int rowsAffected = preparedStatement.executeUpdate();
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null,"Error");
            }
            
            JOptionPane.showMessageDialog(null,"Trip Added Succssfully :)");
            databaseConnection.closeConnection();
        }
    }

