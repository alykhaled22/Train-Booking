    public void AdminInsert(String Firstname,String Lastname,String Useremail,String Userpassword,String Userphone){
        DbConnection databaseConnection = new DbConnection();
        Connection connection = databaseConnection.getConnection();


        if (connection != null) {
            String insertQuery = "INSERT INTO ADMIN(FIRST_NAME,LAST_NAME,EMAIL,PASSWORD,phone) VALUES (?,?,?,?,?)";

            try (PreparedStatement preparedStatement = connection.prepareStatement(insertQuery)) {
                preparedStatement.setString(1, Firstname);
                preparedStatement.setString(2, Lastname);
                preparedStatement.setString(3, Useremail);
                preparedStatement.setString(4, Userpassword);
                preparedStatement.setString(5, Userphone);



                int rowsAffected = preparedStatement.executeUpdate();

                if (rowsAffected > 0) {
                    System.out.println("Data inserted successfully.");
                } else {
                    System.out.println("No rows were affected.");
                }
               
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null,"Error");
            }

            databaseConnection.closeConnection();
        }
    }
