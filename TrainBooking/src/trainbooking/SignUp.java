    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed

        if(Fname.getText().isEmpty() || Lname.getText().isEmpty() || Email.getText().isEmpty() || phone.getText().isEmpty()|| Password.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please Fill all Fields");
        }else{

            String firstname = Fname.getText();
            String lastname = Lname.getText();
            String userEmail = Email.getText();
            String UserPassword = Password.getText();
            String UserPhone = phone.getText();
            int UserIndex = user.getSelectedIndex();

            if(UserIndex == 0){
                AdminInsert(firstname,lastname,userEmail,UserPassword,UserPhone);
            }
            else{
                CustomerInsert(firstname,lastname,userEmail,UserPassword,UserPhone);
            }
                    
            this.setVisible(false);
            new Login().setVisible(true);
        }

    }//GEN-LAST:event_jToggleButton1ActionPerformed

